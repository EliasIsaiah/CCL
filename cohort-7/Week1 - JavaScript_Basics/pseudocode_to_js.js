/* Algorithm:
 * GIVEN: two numbers
 * add the two numbers together
 * divide the result by 2.
 * if the result is greater than 10 then console.log "this number is greater than 10"
 */

// NOUN: person, place, or thing OR (In the context of javascript and pseudocode) VARIABLE
// VERB: action, OPERATION, or a FUNCTION CALL

let num1 = 1000;
let num2 = 4000;
let result;

result = num1 + num2;
answer_divided_by_two = result / 2;

if (answer_divided_by_two > 10) {
  console.log("this number is greater than 10");
}
